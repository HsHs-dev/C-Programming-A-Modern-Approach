/*
6. Repeat Exercise 5 for C99.
*/

#include <stdio.h>

int main(void) {

    
    printf("8 %% 5 = %d\n", 8 % 5);
    printf("-8 %% 5 = %d\n", -8 % 5);
    printf("8 %% -5 = %d\n", 8 % -5);
    printf("-8 %% -5 = %d\n", -8 % -5);


    return 0;
}

/*
8 % 5 = 3
-8 % 5 = -3
8 % -5 = 3
-8 % -5 = -3
*/